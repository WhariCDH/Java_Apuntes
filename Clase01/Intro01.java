//Todos los programas de Java tienen una clase pública 
public class Intro01{
    /*
    Todos los programas tienen una función principal(main) que inicia la ejecución del programa
    *Static: No necesita que se cree un objeto para ser ejercutado. 
    *Void: No retorna ningun valor .
    *Main: Nombre especial que indica el punto de inicio del programa .
    *String[] args: Permite recibir argumentos desde las lineas de comandos .
    */
    public static void main(String [] args){
        //Aqui puedes poner todos los codigos que quieras ejecutar 
        //print: juntos
        System.out.print("Hola mundo ");
        System.out.print("Bienvenido a Skill");
        //println: separados
        System.out.println("Hola mundo");
        System.out.println("Bienvenido a Skinn");
    }
}